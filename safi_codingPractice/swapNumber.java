package safi_codingPractice;

public class swapNumber {

    public static void main(String[] args) {

        int a = 5;
        int b = 8;

        a = a + b;//13

        b = a - b; //5

        a = a -b;// 8

        System.out.println("a is = "+ a);
        System.out.println("b is = "+ b);
    }
}
